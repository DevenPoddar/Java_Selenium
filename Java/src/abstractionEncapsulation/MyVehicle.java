package abstractionEncapsulation;
public abstract class MyVehicle {
    abstract void startEngine();
}

class Car1 extends MyVehicle {
    void startEngine() {
        System.out.println("Car engine started with key ignition.");
    }
}

class Bike extends MyVehicle {
    void startEngine() {
        System.out.println("Bike engine started with self-start button.");
    }
 
}
class Bulldozer extends MyVehicle {
	void startEngine() {
		System.out.println("Bulldozer engine started with self-start button.");
	}
}

